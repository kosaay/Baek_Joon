import java.util.Scanner;

public class Main {
    public static void main(String[] args) {

        Scanner fog = new Scanner(System.in);

        int a = fog.nextInt();
        int b = fog.nextInt();

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
    }
    
}
